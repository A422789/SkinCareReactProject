const mongoose = require('mongoose');


const paymentTypeSchema = new mongoose.Schema(
  {
    id: {
      type: String,
      required: true,
      unique: true,
    },
    name: {
      type: String,
      required: true,
      unique: true,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

const PaymentType = mongoose.model('PaymentType', paymentTypeSchema);

module.exports = PaymentType;
